season = input("Назви свою улюблену пору року? ")

if season == "весна":
    print("Прокидається природа!")
elif season == "літо":
    print("Час відпочинку на морі!")
elif season == "осінь":
    print("Дерева змінюють колір!")
elif season == "зима":
    print("Можна гратися в сніжки!")
else:
    print("Такої пори року не існує.")
